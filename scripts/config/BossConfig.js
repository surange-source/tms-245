function getMonster(monsterStatus, bm){
    if (null == monsterStatus || null == bm || null == monsterStatus.ID) {
        return null;
    }
    var monster = bm.getMonster(monsterStatus.ID);

    // 設置坐標
    if (monsterStatus.Position != null) {
        monster.setPosition(monsterStatus.Position);
    }

    // 更改屬性
    if (monsterStatus.LEVEL != null || monsterStatus.HP != null || monsterStatus.EXP != null || monsterStatus.WATK != null || monsterStatus.MATK != null || monsterStatus.PDR != null || monsterStatus.MDR != null || monsterStatus.ACC != null || monsterStatus.EVA != null) {
        var changeStats = monster.getForcedMobStat();
        if (changeStats == null) {
            monster.setForcedMobStat(monster.getStats());
            changeStats = monster.getForcedMobStat();
        }
        if (monsterStatus.LEVEL != null) {
            changeStats.setLevel(Math.max(monsterStatus.LEVEL, 1));
        }
        if (monsterStatus.HP != null) {
            monster.changeHP(Math.max(monsterStatus.HP, 0));
        }
        if (monsterStatus.EXP != null) {
            changeStats.setExp(Math.max(monsterStatus.EXP, 0));
        }
        if (monsterStatus.WATK != null) {
            changeStats.setWatk(Math.max(monsterStatus.WATK, 0));
        }
        if (monsterStatus.MATK != null) {
            changeStats.setMatk(Math.max(monsterStatus.MATK, 0));
        }
        if (monsterStatus.PDR != null) {
            changeStats.setPDRate(Math.max(monsterStatus.PDR, 0));
        }
        if (monsterStatus.MDR != null) {
            changeStats.setMDRate(Math.max(monsterStatus.MDR, 0));
        }
        if (monsterStatus.ACC != null) {
            changeStats.setAcc(Math.max(monsterStatus.ACC, 0));
        }
        if (monsterStatus.EVA != null) {
            changeStats.setEva(Math.max(monsterStatus.EVA, 0));
        }
        if (monsterStatus.PUSHED != null) {
            changeStats.setPushed(Math.max(monsterStatus.PUSHED, 0));
        }
        if (monsterStatus.SPEED != null) {
            changeStats.setSpeed(Math.max(monsterStatus.SPEED, 0));
        }
        
        monster.setForcedMobStat(changeStats);
    }

    // 更改大小
    if (monsterStatus.SCALE != null) {
        monster.setScale(monsterStatus.SCALE);
    }

    return monster;
}